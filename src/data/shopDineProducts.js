export function formatLkr(amount) {
  try {
    return new Intl.NumberFormat('en-LK', {
      style: 'currency',
      currency: 'LKR',
      maximumFractionDigits: 0,
    }).format(amount);
  } catch {
    return `LKR ${Math.round(Number(amount) || 0).toLocaleString()}`;
  }
}
